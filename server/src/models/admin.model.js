import mongoose, {Schema} from "mongoose";

const adminSchema = new Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
        trim: true
    },
    role: {
        type: String,
        default: 'admin'
    }
}, {timestamps: true})

const Admin = mongoose.model("Admin", adminSchema)
export default Admin